package com.aspose.threed;

/**
 * Animation binding point.
 */
public enum BindPoint {
    /**
     * Bind to the center of the object.
     */
    Center,

    /**
     * Bind to the pivot point.
     */
    Pivot,

    /**
     * Bind to the origin.
     */
    Origin,

    /**
     * Bind to the geometry center.
     */
    GeometryCenter
}
