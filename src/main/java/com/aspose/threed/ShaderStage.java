package com.aspose.threed;

/**
 * A shader stage for rendering operations.
 */
public final class ShaderStage {
    /**
     * Vertex shader stage.
     */
    public static final int VERTEX_SHADER = 0x1;
    
    /**
     * Fragment shader stage.
     */
    public static final int FRAGMENT_SHADER = 0x2;
    
    /**
     * Geometry shader stage.
     */
    public static final int GEOMETRY_SHADER = 0x4;
    
    /**
     * Compute shader stage.
     */
    public static final int COMPUTE_SHADER = 0x8;

    /**
     * Constructor of ShaderStage.
     */
    public ShaderStage() {
    }
}
