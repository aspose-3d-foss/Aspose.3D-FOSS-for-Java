package com.aspose.threed;

import java.io.Serializable;

public class Vector2 implements Serializable {
    public double x;
    public double y;

    public Vector2() {
        this.x = 0;
        this.y = 0;
    }

    public Vector2(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
