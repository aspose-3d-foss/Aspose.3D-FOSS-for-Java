package com.aspose.threed;

/**
 * The semantic of the vertex field.
 */
public enum VertexFieldSemantic {
    /**
     * Position data
     */
    POSITION,
    /**
     * Binormal vector
     */
    BINORMAL,
    /**
     * Normal vector
     */
    NORMAL,
    /**
     * Tangent vector
     */
    TANGENT,
    /**
     * Texture UV coordinate
     */
    UV,
    /**
     * Vertex color
     */
    VERTEX_COLOR,
    /**
     * Vertex crease
     */
    VERTEX_CREASE,
    /**
     * Edge crease
     */
    EDGE_CREASE,
    /**
     * User data
     */
    USER_DATA,
    /**
     * Visibility
     */
    VISIBILITY,
    /**
     * Specular
     */
    SPECULAR,
    /**
     * Weight
     */
    WEIGHT,
    /**
     * Morph position
     */
    MORPH_POSITION,
    /**
     * Morph normal
     */
    MORPH_NORMAL
}
