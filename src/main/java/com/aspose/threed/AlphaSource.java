package com.aspose.threed;

/**
 * Defines whether the texture contains the alpha channel.
 */
public enum AlphaSource {
    /**
     * No alpha source.
     */
    None,
    
    /**
     * Alpha from pixel data.
     */
    PixelAlpha,
    
    /**
     * Fixed alpha value.
     */
    FixedValue;
}
