package com.aspose.threed;

/**
 * NURBS types.
 */
public enum NurbsType {
    /**
     * The NurbsCurve is an open curve.
     */
    OPEN,
    /**
     * The NurbsCurve is a closed curve has its last control point equals to its first one.
     */
    CLOSED,
    /**
     * The NurbsCurve is a periodic curve.
     */
    PERIODIC
}
